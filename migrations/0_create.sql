CREATE TABLE product(`id` INTEGER PRIMARY KEY, `name` TEXT NOT NULL, `description` TEXT NOT NULL, `price` INTEGER NOT NULL, `image` BINARY);
CREATE TABLE cart(`id` INTEGER NOT NULL, `product_id` INTEGER NOT NULL, `quantity` INTEGER NOT NULL, `user_id` INTEGER);
CREATE TABLE user(`id` INTEGER PRIMARY KEY, `name` TEXT NOT NULL, `email` TEXT NOT NULL, `password` TEXT NOT NULL);
